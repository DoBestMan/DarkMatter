export const create = (address, provider) => {
  const contract = new Contract(address, abi, provider);

  // READ
  const balanceOf = (address) =>
    contract.balanceOf(address).then(utils.formatEther);

  const allowance = (owner, spender) => contract.allowance(owner, spender); // owner (current user address), spender (contract address)

  // WRITE
  const approve = (spender, value) => contract.approve(spender, value); // spender (contract address), value (ethers.utils.parseEther(99999))

  const decreaseAllowance = (spender, subtractedValue) =>
    contract.decreaseAllowance(spender, subtractedValue); // spender (contract address), subtractedValue (ethers.utils.parseEther(12345))

  return {
    address,
    balanceOf,
    allowance,
    approve,
    decreaseAllowance,
  };
};
